void SpiInit()
{
	DDR_SPI_PORT |= (1<<MOSI) | (1<<SCK) | (1<<SS);
	DDR_SPI_PORT &= ~(1<<MISO);
	SPI_PORT |= (1<<MOSI) | (1<<SCK) | (1<<SS);
	SPI_PORT &= ~(1<<MISO);
	SPCR = (1<<SPE) | (1<<MSTR)  | (1<<CPOL) | (1<<CPHA);	
}

void LcdInit ( void )
{
	CONTROL_DDR |= (1<<RST) | (1<<SCE) | (1<<DC);
	CONTROL_PORT |= (1<<RST) | (1<<SCE) | (1<<DC);
	SpiInit();
	CONTROL_PORT &= ~(1<<RST);	//reset LCD
    _delay_ms(250);
    CONTROL_PORT |= (1<<RST);
    LcdWriteCommand( 0x21 );  // LCD Extended Commands.
    LcdWriteCommand( 0xB9 );  // Set LCD Vop (Contrast).
    LcdWriteCommand( 0x06 );  // Set Temp coefficent.
    LcdWriteCommand( 0x14 );  // LCD bias mode 1:48.
    LcdWriteCommand( 0x20 );  // LCD Standard Commands, Horizontal addressing mode.
    LcdWriteCommand( 0x0C );  // LCD in normal mode.
	LcdClear();
}

void LcdWriteCommand ( unsigned char command )
{
    CONTROL_PORT &= ~(1<<SCE);	  //enable LCD
	CONTROL_PORT &= ~(1<<DC);	  //set LCD in command mode
    //  Send data to display controller.
    SPDR = command;
    //  Wait until Tx register empty.
    while ( !(SPSR &(1<<SPIF)) );
    CONTROL_PORT |= (1<<SCE);;   	 //disable LCD
}

void LcdWriteData ( unsigned char Data )
{
    CONTROL_PORT &= ~(1<<SCE);	  //enable LCD
	CONTROL_PORT |= (1<<DC);;	  //set LCD in Data mode
    //  Send data to display controller.
    SPDR = Data;
    //  Wait until Tx register empty.
    while ( !(SPSR & (1<<SPIF)) );
    CONTROL_PORT |= (1<<SCE);;   	 //disable LCD
}

void LcdClear ( void )
{
    unsigned char i,j;

	LcdGotoXY (0,0);  	//start with (0,0) position
    for(i=0; i<8; i++)
	  for(j=0; j<90; j++)
	     LcdWriteData( 0x00 );
    LcdGotoXY (0,0);	//bring the XY position back to (0,0)
}

void LcdGotoXY ( unsigned char x, unsigned char y )
{
    LcdWriteCommand (0x80 | x);   //column
	LcdWriteCommand (0x40 | y);   //row
}

void LcdWriteChar (unsigned char ch)
{
   unsigned char j;

   LcdWriteData(0x00);
   for(j=0; j<5; j++)
     LcdWriteData(pgm_read_byte(&smallFont[(ch-32)*5+j]) );
   LcdWriteData( 0x00 );
}

void LcdWriteCharInverted (unsigned char ch)
{
   unsigned char j;

   LcdWriteData(0xFF);
   for(j=0; j<5; j++)
     LcdWriteData(~pgm_read_byte(&smallFont[(ch-32)*5+j]) );
   LcdWriteData( 0xFF );
}

void LcdWriteString (char *string )
{
    while (*string)
        LcdWriteChar(*string++ );
}

void LcdWriteStringln (char *string )
{
	unsigned char length = (DISPLAY_WIDTH/6) - 2;
    while ( (*string) && length )
	{
        LcdWriteChar(*string++ );
		length--;
	}
	while(length)		// fill up empty spaces with blank character....
	{
		LcdWriteChar(' ');
		length--;
	}
}

void LcdWriteStringInverted (char *string )
{
    while ( *string) 
	    LcdWriteCharInverted( *string++ );
}

void LcdWriteStringInvertedln (char *string )
{
	unsigned char Length = (DISPLAY_WIDTH/6) - 2;
    while ( (*string) && Length)
	{    
	    LcdWriteCharInverted( *string++ );
		Length--;
	}
	while(Length)	// fill up empty spaces with blank character....
	{
		LcdWriteCharInverted(' ');
		Length--;
	}
}

void LcdSetPixel(unsigned char x, unsigned char y)
{
	unsigned char Column = y/8;
	unsigned char Pixel = y%8;
	
	if(x>DISPLAY_WIDTH)
		x = DISPLAY_WIDTH;
	if(y>DISPLAY_HEIGHT)
		y = DISPLAY_HEIGHT;
		
	LcdGotoXY(x, Column);
	LcdWriteData((1<<Pixel));
}



void LcdClearColumn(unsigned char x)
{
	unsigned char i=0;
	for(i=0;i<6;i++)
	{
		LcdGotoXY(x, i);
		LcdWriteData(0);
	}
}
